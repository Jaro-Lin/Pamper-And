package com.nyw.pets.activity.util;

/**
 * 课程列表util
 */
public class GetCurriculumListUtil {
    String videoPath;
    String id;
    String title;
    String learningNumber;
    String path;
    String img;
    //主要分类
    String type;

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }

    public String getVideoPath() {
        return videoPath;
    }

    public void setVideoPath(String videoPath) {
        this.videoPath = videoPath;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getLearningNumber() {
        return learningNumber;
    }

    public void setLearningNumber(String learningNumber) {
        this.learningNumber = learningNumber;
    }
}
