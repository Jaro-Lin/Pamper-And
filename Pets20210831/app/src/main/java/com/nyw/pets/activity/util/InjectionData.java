package com.nyw.pets.activity.util;

public class InjectionData {
    String vaccin_time;
    String vaccin_count;
    String vaccin_type;

    public String getVaccin_time() {
        return vaccin_time;
    }

    public void setVaccin_time(String vaccin_time) {
        this.vaccin_time = vaccin_time;
    }

    public String getVaccin_count() {
        return vaccin_count;
    }

    public void setVaccin_count(String vaccin_count) {
        this.vaccin_count = vaccin_count;
    }

    public String getVaccin_type() {
        return vaccin_type;
    }

    public void setVaccin_type(String vaccin_type) {
        this.vaccin_type = vaccin_type;
    }
}
